public class RowSpot 
{

	public static void main(String[] args) 
	{
		for(int i=1;i<=3;i++)//loops for rows
		{
			for(int j=1;j<=5;j++)//loops for spots
			{
				System.out.println("planting seed in row"+i+" and "+"spot"+j);
			}
		}
          System.out.println("Plantation of seeds is completed");
          
	}

}
